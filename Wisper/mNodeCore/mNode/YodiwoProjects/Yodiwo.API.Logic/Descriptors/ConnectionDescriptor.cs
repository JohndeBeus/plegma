using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace Yodiwo.Logic.Descriptors
{
    public class ConnectionDescriptor
    {
        //-------------------------------------------------------------------------------------------------------------------------
        [DB_IgnoreIfDefault]
        public int FromID;
        //-------------------------------------------------------------------------------------------------------------------------
        [DB_IgnoreIfDefault]
        public int FromIndex;
        //-------------------------------------------------------------------------------------------------------------------------
        [DB_IgnoreIfDefault]
        public int ToID;
        //-------------------------------------------------------------------------------------------------------------------------
        [DB_IgnoreIfDefault]
        public int ToIndex;
        //-------------------------------------------------------------------------------------------------------------------------
        [DB_IgnoreIfDefault]
        [DB_IgnoreIfEmpty]
        public List<Vertex> Vertices;
        //-------------------------------------------------------------------------------------------------------------------------
    }

    public class Vertex
    {
        //-------------------------------------------------------------------------------------------------------------------------
        [DB_IgnoreIfDefault]
        public float x;
        //-------------------------------------------------------------------------------------------------------------------------
        [DB_IgnoreIfDefault]
        public float y;
        //-------------------------------------------------------------------------------------------------------------------------
    }
}
